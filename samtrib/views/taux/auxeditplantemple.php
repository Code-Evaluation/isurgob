	function btGrabarClick(){
		
			err = "";
			
			if ($("#txNombre").val()=="")
			{
				err += "<li>Ingrese un nombre</li>";
			}

			if (err == "")
			{
				$('#caja_id').prop('disabled',false);
				$("#frmTemple").submit();
			}else {
				$.pjax.reload(
				{
					container:"#divError",
					data:{
							error:err
						},
					method:"POST"
				});
			}
	}
		
